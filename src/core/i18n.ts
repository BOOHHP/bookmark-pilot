import type { Settings } from '../types';

export type ResolvedLang = 'zh' | 'en';

export function resolveLang(language: Settings['language']): ResolvedLang {
  if (language === 'auto') {
    return navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en';
  }
  return language;
}

