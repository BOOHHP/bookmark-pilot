export type Dict = typeof zh;

export function t(language: Settings['language']): Dict {
  return resolveLang(language) === 'zh' ? zh : en;
}

/** 应用颜色模式：设置 html[data-theme]，system 时跟随系统 */
export function applyColorMode(mode: Settings['colorMode']): void {
  const dark =
    mode === 'dark' ||
    (mode === 'system' && window.matchMedia('(prefers-color-scheme: dark)').matches);
  document.documentElement.dataset.theme = dark ? 'dark' : 'light';
}
